import Foundation

/// 溢出运算符（Overflow Operator）
func test01() {
    print("-------------test01-------------------")
    /*
     swift的算数运算符出现溢出时会抛出运行时错误
     swift有溢出运算符（&+, &-, &*），用来支持溢出运算
     */
    var min = UInt8.min
    print(min, min &- 1)
    var max = UInt8.max
    print(max, max &+ 1, max &* 2)
    
    print(Int8.min, Int8.max)
//    print(Int8.max + 1)
//    var res: Int = Int8.max + 1
}
test01()

